// spread operator:

// The spread operator is a syntax that allows us to expand or spread elements of an array or object.



const numbers = [1,2,3,4,5];

const newNumbers = [...numbers];

console.log(newNumbers);


// spread operator in objects:

const person = {
    name: 'John',
    age: 20
}

const newPerson = {...person};

console.log(newPerson);



